def main():
    # Merge FR
    fr_path = BASE / "fr.json"
    fr_data = json.loads(fr_path.read_text(encoding="utf-8"))
    deep_merge(fr_data, PATCH_FR)
    fr_path.write_text(json.dumps(fr_data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print("Updated fr.json")

    en_path = BASE / "en.json"
    en_data = json.loads(en_path.read_text(encoding="utf-8"))
    deep_merge(en_data, PATCH_EN)
    en_path.write_text(json.dumps(en_data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print("Updated en.json")

    # Other locales: merge English patch (same as EN for new keys) — align with prior project approach
    for name in ["sw.json", "ln.json", "kg.json", "lu.json"]:
        p = BASE / name
        data = json.loads(p.read_text(encoding="utf-8"))
        deep_merge(data, PATCH_EN)
        p.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
        print(f"Updated {name} (EN text for new keys)")


if __name__ == "__main__":
    main()
